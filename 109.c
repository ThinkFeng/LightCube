void produce_empty(uchar *a,char n)//0<n<=8,注意n不能为0
{
	char j;
	uchar p;
	for(j=0;j<64;j++)
		a[j]=0;
	p=0xff>>(8-n);
/************X4列*************/
	a[(n-1)*8+n-1]=p;
	a[(n-1)*8]=p;
	a[0]=p;
	a[n-1]=p;
	for(j=0;j<n;j++)
	{
		p=(0x01|(0x01<<(n-1)));
/************竖直4列*************/
		a[j*8]|=p;
		a[j*8+n-1]|=p;
/************Y4列*************/
		a[j]|=p;
		a[(n-1)*8+j]|=p;
	}
}
