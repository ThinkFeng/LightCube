void move(uchar *a,char kind,char direction,char length)//length不能为8
//kind=0 x轴,1 y轴,2 z轴；direction=0,负向,1正向
{
	char i,j;
	if(kind==0)
	{
		if(direction==1)
			for(i=0;i<64;i++)
				a[i]<<=length;
		else
			for(i=0;i<64;i++)
				a[i]>>=length;	
	}
	else if(kind==1)
	{
		if(direction==1)
			for(j=0;j<8;j++)
			{
				for(i=7;i>=length;i--)
					a[i+j*8]=a[i-length+j*8];
				for(i=0;i<length;i++)
					a[i+j*8]=0;			
			}	
		else
			for(j=0;j<8;j++)
			{
				for(i=0;i<length;i++)
					a[i+j*8]=a[i+length+j*8];
				for(i=(8-length);i<8;i++)
					a[i+j*8]=0;			
			}			
	}
	else
	{
		if(direction==1)
			for(j=0;j<8;j++)
			{
				for(i=7;i>=length;i--)
					a[j+i*8]=a[j+(i-length)*8];
				for(i=0;i<length;i++)
					a[j+i*8]=0;			
			}	
		else
			for(j=0;j<8;j++)
			{
				for(i=0;i<(8-length);i++)
					a[j+i*8]=a[j+(i+length)*8];
				for(i=(8-length);i<8;i++)
					a[j+i*8]=0;			
			}	
	}
}
